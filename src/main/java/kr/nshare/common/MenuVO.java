	private String menu_id = "";
	private String parent_id = "";
	private String name = "";
	private String sdate = "";
	private String sub_url = "";
	private String sub_menu_flag = "";
	private String url = "";
	private String type = "";
	private String sort = "";
	private String level_type = "";
	private String lev = "";
	private String show = "";

